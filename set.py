'''
Set is a collection which is unordered, unchangeable*,
 and unindexed. No duplicate members.
'''
# set declare:
set_01 = { 'tree', True, 120.25, False, 'apple', 12}
print(set_01)
print('-------------------------')

#length of the set:
print(len(set_01))
print('-------------------------')

#type of set:
print(type(set_01))
print('-------------------------')

# use set() constructor:
set_02= set_01
print(set_02)
print('-------------------------')

# add():
set_01.add('addItem')
print(set_01)
print('-------------------------')

